-- MARRIAGE:ACCEPT
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNetEvent("marriage:Accept")
AddEventHandler("marriage:Accept",function(Sex,Name)
	SendNUIMessage({ Action = "Accept", Payload = { Sex,Name } })
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- MARRIAGE:REJECT
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNetEvent("marriage:Reject")
AddEventHandler("marriage:Reject",function(Name)
	SendNUIMessage({ Action = "Reject", Payload = Name })
end)